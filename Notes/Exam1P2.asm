.ORIG X3000

AND R0, R0, #0
AND R1, R1, #0
AND R2, R2, #0
AND R3, R3, #0

LD R2, ASCII_OFFSET

START

;GET FIRST NUMBER
LEA R0, INPUT_NUM_PROMPT
PUTS

GETC
OUT

ADD R0, R0, R2
ST R0, NUM_1

;GET SECOND NUMBER
LEA R0, INPUT_NUM_PROMPT
PUTS

GETC
OUT

ADD R0, R0, R2
ST R0, NUM_2

;DIVIDE
LD R0, NUM_1
LD R1, NUM_2
LD R2, DIV_COUNT

DIVIDE_LOOP
ADD R2, R2, #1
ADD R3, R3, R1
AND R4, R4, #0
NOT R4, R3
ADD R4, R4, #1
ADD R4, R0, R4
BRp DIVIDE_LOOP
BRz FINISH

NEG_CHECK
ADD R2, R2, #-1

FINISH
ST R2, ANSWER
LEA R0, OUTPUT
PUTS

BRnzp START



HALT

INPUT_NUM_PROMPT .STRINGZ "\nPlease enter a one-digit number:"
ASCII_OFFSET .FILL #-48
NUM_1 .FILL #0
NUM_2 .FILL #0
DIV_COUNT .FILL #0
ANSWER .FILL #0
OUTPUT .STRINGZ "\nThe answer is in R3. I do not remember how to output a string with variables in it."

.END