.ORIG X3000

START_LOOP
LD R0, VAR_I
LD R1, NUM_ITER
NOT R1, R1
ADD R1, R1, #1
ADD R0, R0, R1
BRzp END_LOOP

LEA R0, PROMPT
PUTS

GETC
OUT

LD R1, NEG_ASCII
ADD R0, R0, R1

LEA R1, ARRAY
LD R2, VAR_I
ADD R1, R1, R2
STR R0, R1, #0

LD R0, VAR_I
ADD R0, R0, #1
ST R0, VAR_I
BRnzp START_LOOP
END_LOOP

HALT

NEG_ASCII .FILL #-48

VAR_I .FILL #0

ARRAY .BLKW #10
NUM_ITER .FILL #10

PROMPT .STRINGZ "\nPlease enter a number (0-9): "

.END