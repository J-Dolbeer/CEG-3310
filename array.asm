.ORIG X3000

START_LOOP
LD R0, VAR_I
LD R1, NUM_ITER
NOT R1, R1
ADD R1, R1, #1
ADD R0, R0, R1
BRzp END_LOOP

; GET INPUT INTO R0
GETC
OUT

; CONVERT FROM ASCII TO NUMBER
LD R1, NEG_ASCII
ADD R0, R0, R1

; ARR[i] = R0
LD R1, ARRAY_ADDR
LD R2, VAR_I
ADD R1, R1, R2
STR R0, R1, #0

LD R0, VAR_I
ADD R0, R0, #1
ST R0, VAR_I
BRnzp  START_LOOP
END_LOOP

HALT

NEG_ASCII .FILL #-48
ARRAY_ADDR .FILL X4000

VAR_I .FILL #0

NUM_ITER .FILL #10

.END